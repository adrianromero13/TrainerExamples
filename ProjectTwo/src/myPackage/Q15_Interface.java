// interfaces are like class templates: 
// they define what methods a class should have, but do not define those methods with code.
// only stubs. (a design level artifact to make implementations on)
public interface Q15_Interface {

	double addition(double a, double b);

	double subtraction(double a , double b);
	
	double multiplication(double a, double b);
	
	double division(double a, double b);
}
